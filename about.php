        $('.scroll_down').css('transition', '0.3s');
        $('.scroll_down').css('top', '880px');
    })

    $('.scroll_down').on('click', function() {
        if ($(window).width() > 1000) {
            $('html, body').animate({
                scrollTop: $('#second').offset().top + 100
